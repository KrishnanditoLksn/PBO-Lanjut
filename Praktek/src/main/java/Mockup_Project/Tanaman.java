package Mockup_Project;

/**
 * @author ASUS
 */
public class Tanaman {

    private int stock, price;
    private String plantName;

    public Tanaman(int stock, String plantName, int price) {
        this.stock = stock;
        this.plantName = plantName;
        this.price = price;
    }

    public int getStock() {
        return stock;
    }

    public void setStock(int stock) {
        this.stock = stock;
    }

    public int getPrice() {
        return price;
    }

    public void setPrice(int price) {
        this.price = price;
    }

    public String getPlantName() {
        return plantName;
    }

    public void setPlantName(String plantName) {
        this.plantName = plantName;
    }

}
